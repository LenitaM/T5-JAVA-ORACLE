package teste;

public class TesteArrayString {

    public static void main(String[] args) {

        for(int i = 0; i < args.length; i++) {
            System.out.println(args[i]); //Array de referencias
        }
        /*
        Assim, temos uma forma de interagir com o programa Java a partir da linha de comando.
         Chamamos a aplicação e, simultaneamente, é possível definirmos parâmetros. Isso tem várias utilidades.
Pode servir, por exemplo, para habilitar alguma funcionalidade, entre várias outras configurações possíveis.*/
    }
}
